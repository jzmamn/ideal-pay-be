-- Generates next sequential loan reference number: LNA-YYYY-NNNNN
DROP PROCEDURE IF EXISTS `sp_generate_loan_no`;
DELIMITER ;;
CREATE DEFINER=`root`@`localhost` PROCEDURE `sp_generate_loan_no`(
    IN  p_year  INT,
    OUT p_loan_no VARCHAR(25)
)
BEGIN
    DECLARE v_next_seq INT DEFAULT 1;

    SELECT IFNULL(MAX(
        CAST(SUBSTRING_INDEX(loan_no, '-', -1) AS UNSIGNED)
    ), 0) + 1
    INTO v_next_seq
    FROM loan_application
    WHERE loan_no LIKE CONCAT('LNA-', p_year, '-%')
      AND id > 0;

    SET p_loan_no = CONCAT('LNA-', p_year, '-', LPAD(v_next_seq, 5, '0'));
END ;;
DELIMITER ;

-- â”€â”€ sp_create_loan_schedule â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
-- Generates loan_installment rows for an APPROVED application.
-- Call after updating status to APPROVED.
DROP PROCEDURE IF EXISTS `sp_create_loan_schedule`;
DELIMITER ;;
CREATE DEFINER=`root`@`localhost` PROCEDURE `sp_create_loan_schedule`(
    IN p_application_id BIGINT,
    IN p_created_by     BIGINT
)
BEGIN
    DECLARE v_installments       INT;
    DECLARE v_monthly_installment DECIMAL(15,2);
    DECLARE v_start_month        VARCHAR(20);
    DECLARE v_cur_year           INT;
    DECLARE v_cur_month          INT;
    DECLARE v_i                  INT DEFAULT 1;
    DECLARE v_payroll_month      VARCHAR(20);

    -- Fetch application header
    SELECT installments, monthly_installment, start_payroll_month
    INTO   v_installments, v_monthly_installment, v_start_month
    FROM   loan_application
    WHERE  id = p_application_id;

    SET v_cur_year  = CAST(SUBSTRING_INDEX(v_start_month, '-', 1)  AS UNSIGNED);
    SET v_cur_month = CAST(SUBSTRING_INDEX(v_start_month, '-', -1) AS UNSIGNED);

    -- Remove any existing schedule (re-generate on re-approval)
    DELETE FROM loan_installment
    WHERE  application_id = p_application_id AND id > 0;

    -- Insert one row per installment
    WHILE v_i <= v_installments DO
        SET v_payroll_month = CONCAT(v_cur_year, '-', LPAD(v_cur_month, 2, '0'));

        INSERT INTO loan_installment
          (application_id, installment_no, payroll_month,
           scheduled_amount, paid_amount, status,
           created_by, modified_by)
        VALUES
          (p_application_id, v_i, v_payroll_month,
           v_monthly_installment, 0.00, 'PENDING',
           p_created_by, p_created_by);

        -- Advance month
        SET v_cur_month = v_cur_month + 1;
        IF v_cur_month > 12 THEN
            SET v_cur_month = 1;
            SET v_cur_year  = v_cur_year + 1;
        END IF;

        SET v_i = v_i + 1;
    END WHILE;

    -- Initialise outstanding balance
    UPDATE loan_application
    SET    outstanding_balance = principal_amount,
           paid_installments   = 0
    WHERE  id = p_application_id;
END ;;
DELIMITER ;

-- â”€â”€ sp_loan_application_summary â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
-- Per-employee loan summary for a given status or ALL.
DROP PROCEDURE IF EXISTS `sp_loan_application_summary`;
DELIMITER ;;
CREATE DEFINER=`root`@`localhost` PROCEDURE `sp_loan_application_summary`(
    IN p_status VARCHAR(20)    -- pass NULL for all statuses
)
BEGIN
    SELECT
        e.id                                   AS emp_id,
        e.employee_no,
        e.payroll_name,
        l.name                                 AS loan_type,
        la.loan_no,
        la.applied_date,
        la.principal_amount,
        la.installments,
        la.monthly_installment,
        la.paid_installments,
        la.outstanding_balance,
        la.status,
        la.start_payroll_month,
        la.disbursed_date,
        la.closed_date,
        la.remarks
    FROM  loan_application la
    JOIN  employee e ON la.emp_id      = e.id
    JOIN  loan     l ON la.loan_type_id = l.id
    WHERE la.id > 0
      AND (p_status IS NULL OR la.status = p_status)
    ORDER BY la.applied_date DESC, e.employee_no;
END ;;
DELIMITER ;

-- â”€â”€ sp_employee_loan_statement â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
-- Full installment statement for one employee application.
DROP PROCEDURE IF EXISTS `sp_employee_loan_statement`;
DELIMITER ;;
CREATE DEFINER=`root`@`localhost` PROCEDURE `sp_employee_loan_statement`(
    IN p_application_id BIGINT
)
BEGIN
    SELECT
        la.loan_no,
        e.employee_no,
        e.payroll_name,
        l.name                  AS loan_type,
        la.principal_amount,
        la.installments,
        la.monthly_installment,
        la.outstanding_balance,
        la.status               AS application_status,
        li.installment_no,
        li.payroll_month,
        li.scheduled_amount,
        li.paid_amount,
        li.status               AS installment_status,
        li.paid_date
    FROM  loan_installment  li
    JOIN  loan_application  la ON li.application_id = la.id
    JOIN  employee          e  ON la.emp_id          = e.id
    JOIN  loan              l  ON la.loan_type_id    = l.id
    WHERE li.application_id = p_application_id
      AND li.id > 0
    ORDER BY li.installment_no;
END ;;
DELIMITER ;
