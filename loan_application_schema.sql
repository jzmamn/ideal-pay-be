-- ============================================================
-- 1. Enhance loan master table
--    Add eligibility and limit fields
-- ============================================================
ALTER TABLE `loan`
  ADD COLUMN `max_amount`       decimal(15,2) NOT NULL DEFAULT '0.00'
      COMMENT 'Maximum amount an employee can apply for'
      AFTER `amount`,
  ADD COLUMN `max_installments` int           NOT NULL DEFAULT 0
      COMMENT 'Maximum repayment period in months'
      AFTER `max_amount`,
  ADD COLUMN `min_service_months` int         NOT NULL DEFAULT 0
      COMMENT 'Minimum months of service required to be eligible'
      AFTER `max_installments`;

-- Update the default row to reflect the new columns cleanly
UPDATE `loan`
SET max_amount = 0.00, max_installments = 0, min_service_months = 0
WHERE id = -1;

-- ============================================================
-- 2. loan_application
--    One row per application â€” tracks the full lifecycle from
--    PENDING through APPROVED/REJECTED to ACTIVE and CLOSED.
-- ============================================================
DROP TABLE IF EXISTS `loan_application`;

CREATE TABLE `loan_application` (
  `id`                 bigint        NOT NULL AUTO_INCREMENT,

  -- Reference number generated by application (LNA-YYYY-NNNNN)
  `loan_no`            varchar(25)   NOT NULL,

  -- Core relationships
  `emp_id`             bigint        NOT NULL,
  `loan_type_id`       bigint        NOT NULL   COMMENT 'FK â†’ loan.id',

  -- Application details
  `applied_date`       date          NOT NULL,
  `principal_amount`   decimal(15,2) NOT NULL DEFAULT '0.00',
  `installments`       int           NOT NULL DEFAULT 1
                           COMMENT 'Total number of monthly installments',
  `monthly_installment` decimal(15,2) NOT NULL DEFAULT '0.00'
                           COMMENT 'Calculated: principal_amount / installments',
  `start_payroll_month` varchar(20)  NOT NULL
                           COMMENT 'First deduction month e.g. 2026-07',
  `remarks`            varchar(500)  DEFAULT NULL,

  -- Status workflow:
  --   PENDING â†’ APPROVED or REJECTED
  --   APPROVED â†’ ACTIVE (when first installment processed)
  --   ACTIVE   â†’ CLOSED (when all installments paid / manually closed)
  `status`             varchar(20)   NOT NULL DEFAULT 'PENDING'
                           COMMENT 'PENDING | APPROVED | REJECTED | ACTIVE | CLOSED',

  -- Approval / rejection
  `approved_by`        bigint        DEFAULT NULL,
  `approved_date`      datetime      DEFAULT NULL,
  `rejected_by`        bigint        DEFAULT NULL,
  `rejected_date`      datetime      DEFAULT NULL,
  `rejection_reason`   varchar(500)  DEFAULT NULL,

  -- Disbursement
  `disbursed_amount`   decimal(15,2) NOT NULL DEFAULT '0.00',
  `disbursed_date`     date          DEFAULT NULL,

  -- Running totals (updated as installments are paid)
  `paid_installments`  int           NOT NULL DEFAULT 0,
  `outstanding_balance` decimal(15,2) NOT NULL DEFAULT '0.00'
                           COMMENT 'Remaining principal still to be deducted',
  `closed_date`        date          DEFAULT NULL,

  -- Soft delete / active flag
  `is_active`          char(1)       NOT NULL DEFAULT 'Y',

  -- Audit
  `created_by`         bigint        NOT NULL,
  `created_date`       timestamp     NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `modified_by`        bigint        NOT NULL,
  `modified_date`      timestamp     NOT NULL DEFAULT CURRENT_TIMESTAMP
                           ON UPDATE CURRENT_TIMESTAMP,

  PRIMARY KEY (`id`),
  UNIQUE  KEY `uk_loan_application_no`      (`loan_no`),
  KEY `fk_la_emp_id`                        (`emp_id`),
  KEY `fk_la_loan_type_id`                  (`loan_type_id`),
  KEY `fk_la_approved_by`                   (`approved_by`),
  KEY `fk_la_rejected_by`                   (`rejected_by`),
  KEY `fk_la_created_by`                    (`created_by`),
  KEY `fk_la_modified_by`                   (`modified_by`),
  KEY `idx_la_emp_status`                   (`emp_id`, `status`),
  KEY `idx_la_status`                       (`status`),

  CONSTRAINT `fk_la_emp_id`
      FOREIGN KEY (`emp_id`)        REFERENCES `employee` (`id`),
  CONSTRAINT `fk_la_loan_type_id`
      FOREIGN KEY (`loan_type_id`)  REFERENCES `loan` (`id`),
  CONSTRAINT `fk_la_approved_by`
      FOREIGN KEY (`approved_by`)   REFERENCES `usr` (`id`),
  CONSTRAINT `fk_la_rejected_by`
      FOREIGN KEY (`rejected_by`)   REFERENCES `usr` (`id`),
  CONSTRAINT `fk_la_created_by`
      FOREIGN KEY (`created_by`)    REFERENCES `usr` (`id`),
  CONSTRAINT `fk_la_modified_by`
      FOREIGN KEY (`modified_by`)   REFERENCES `usr` (`id`),

  CONSTRAINT `chk_la_status`
      CHECK (`status` IN ('PENDING','APPROVED','REJECTED','ACTIVE','CLOSED')),
  CONSTRAINT `chk_la_active`
      CHECK (`is_active` IN ('Y','N')),
  CONSTRAINT `chk_la_installments`
      CHECK (`installments` >= 1),
  CONSTRAINT `chk_la_principal`
      CHECK (`principal_amount` > 0)

) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci
  COMMENT='Loan application lifecycle â€” one row per employee loan request';

