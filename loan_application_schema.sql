-- ============================================================
-- 3. loan_installment
--    One row per scheduled monthly repayment.
--    Generated automatically when application is APPROVED.
--    Links back to emp_loan when the payroll deduction is made.
-- ============================================================
DROP TABLE IF EXISTS `loan_installment`;

CREATE TABLE `loan_installment` (
  `id`                bigint        NOT NULL AUTO_INCREMENT,
  `application_id`    bigint        NOT NULL   COMMENT 'FK â†’ loan_application.id',
  `installment_no`    int           NOT NULL   COMMENT 'Sequence 1..N',
  `payroll_month`     varchar(20)   NOT NULL   COMMENT 'e.g. 2026-07',

  -- Amounts
  `scheduled_amount`  decimal(15,2) NOT NULL DEFAULT '0.00'
                          COMMENT 'Amount due this installment',
  `paid_amount`       decimal(15,2) NOT NULL DEFAULT '0.00'
                          COMMENT 'Actual amount deducted',

  -- Status:
  --   PENDING  â†’ due but not yet processed
  --   PAID     â†’ fully deducted via payroll
  --   PARTIAL  â†’ partially deducted (manual adjustment)
  --   SKIPPED  â†’ waived or deferred for this month
  `status`            varchar(10)   NOT NULL DEFAULT 'PENDING'
                          COMMENT 'PENDING | PAID | PARTIAL | SKIPPED',

  `paid_date`         date          DEFAULT NULL,

  -- Link to the actual payroll deduction record
  `emp_loan_id`       bigint        DEFAULT NULL
                          COMMENT 'FK â†’ emp_loan.id when deducted in payroll',
  `notes`             varchar(255)  DEFAULT NULL,

  -- Audit
  `created_by`        bigint        NOT NULL,
  `created_date`      timestamp     NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `modified_by`       bigint        NOT NULL,
  `modified_date`     timestamp     NOT NULL DEFAULT CURRENT_TIMESTAMP
                          ON UPDATE CURRENT_TIMESTAMP,

  PRIMARY KEY (`id`),
  UNIQUE  KEY `uk_loan_installment`     (`application_id`, `installment_no`),
  KEY `fk_li_application_id`           (`application_id`),
  KEY `fk_li_emp_loan_id`              (`emp_loan_id`),
  KEY `fk_li_created_by`               (`created_by`),
  KEY `fk_li_modified_by`              (`modified_by`),
  KEY `idx_li_payroll_month`           (`payroll_month`),
  KEY `idx_li_status`                  (`status`),

  CONSTRAINT `fk_li_application_id`
      FOREIGN KEY (`application_id`) REFERENCES `loan_application` (`id`)
      ON DELETE CASCADE,
  CONSTRAINT `fk_li_emp_loan_id`
      FOREIGN KEY (`emp_loan_id`)    REFERENCES `emp_loan` (`id`)
      ON DELETE SET NULL,
  CONSTRAINT `fk_li_created_by`
      FOREIGN KEY (`created_by`)     REFERENCES `usr` (`id`),
  CONSTRAINT `fk_li_modified_by`
      FOREIGN KEY (`modified_by`)    REFERENCES `usr` (`id`),

  CONSTRAINT `chk_li_status`
      CHECK (`status` IN ('PENDING','PAID','PARTIAL','SKIPPED')),
  CONSTRAINT `chk_li_installment_no`
      CHECK (`installment_no` >= 1)

) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci
  COMMENT='Repayment schedule â€” one row per monthly installment per application';

